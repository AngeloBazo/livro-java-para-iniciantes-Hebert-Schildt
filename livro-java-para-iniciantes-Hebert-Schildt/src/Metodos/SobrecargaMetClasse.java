package Metodos;

public class SobrecargaMetClasse {
	
	public static int sobrecarregar (int x) {
		
		int y = x;
				
		System.out.println("Sobrecarga com INT " + y);
		
		return y;
		
	}
	
	
	public static String sobrecarregar (String j) {
		
		String u = j;
		
		System.out.println("Sobrecarga com String " + u);
		
		return u;
		
	}
	

}
